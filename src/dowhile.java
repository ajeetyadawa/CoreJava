
public class dowhile {

	/*
	 * This program written to demonstrate Do While in Java
	 */
	
	public static void main(String[] args) {
		
				int i=1;
				do {
					System.out.println(i);
					i++;
				}while(i<=10);
				int j=25;
				do {
					System.out.println("number ="+j);
					j--;
		
			}while(j>=0);
	}

}
